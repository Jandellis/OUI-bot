package bot;

public class KickMember {
    Long id;
    Long daysUnhappy;
    Long daysNoWork;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getDaysUnhappy() {
        return daysUnhappy;
    }

    public void setDaysUnhappy(Long daysUnhappy) {
        this.daysUnhappy = daysUnhappy;
    }

    public Long getDaysNoWork() {
        return daysNoWork;
    }

    public void setDaysNoWork(Long daysNoWork) {
        this.daysNoWork = daysNoWork;
    }
}
